<h1>Preparing Your Session for Mixdown</h1>
	<p>Please prepare your session using the following steps, otherwise I will not be able to start mixing your project until you have done so:</p>
	<ul>
    <li>Make sure all files are .wav or .aif. Any MIDI files must be bounced/rendered as audio files to be accepted.</li>
    <li>Make sure all files start at 0:00:00 on the time line and run continuously to the end of the part.</li>
    <li>Make sure all audio files have meaningful names such as kick or ohleft or BGV1. Files names such as audio1.wav and audio2.wav have no meaning and will seriously slow down the process while I figure out what is what.</li>
    <li>Make sure the arrangement is the way you want it on the finished project. Although I am perfectly capable of rearranging the parts, that is production, not mixing. If you would like me to provide production services, just let me know and I will give you a quote.</li>
    <li>Dry audio files (no effects) are highly recommended to allow more flexibility with effects. Remember there is no way to remove an effect once it has been printed to the track. For that reason, send a wet and dry version of any “must have” effects.</li>
	</ul>
